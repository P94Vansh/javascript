const month="march"
switch(month){
    case "jan":
        console.log("January")
        break;
    case "feb":
        console.log("Feburary")
        break;
    case "march":
        console.log("March")
        break;
    case "Apr":
        console.log("April")
        break;
    default:
        console.log("Default case mathced")
        break;
}
